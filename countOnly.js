const countOnly = function(allItems, itemsToCount) {
  const resultObj = {};
  for (const item of allItems) {
    for (const key in itemsToCount) {
      if (item === key && itemsToCount[key] === true) {
        resultObj[key] === undefined ? resultObj[key] = 1 : resultObj[key]++;
      }
    }
  }
  return resultObj;
};


const findKeyByValue = function(givenObj, givenValue) {
  for (let objKey in givenObj) {
    if (givenObj[objKey] === givenValue) {
      return objKey;
    }
  }
  return undefined;
};

const bestTVShowsByGenre = {
  sci_fi: "The Expanse",
  comedy: "Brooklyn Nine-Nine",
  drama:  "The Wire"
};

console.log(findKeyByValue(bestTVShowsByGenre,"The Wire"));
console.log(findKeyByValue(bestTVShowsByGenre,"Lion King"));

